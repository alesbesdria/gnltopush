#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	size_t	n;

	n = 0;
	while (src[n])
	{
		dst[n] = src[n];
		n++;
	}
	dst[n] = '\0';
	return (dst);
}
