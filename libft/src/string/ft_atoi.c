#include "libft.h"

int		ft_atoi(const char *str)
{
	int		n;
	int		s;

	n = 0;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	if ((s = (*str == '-' ? -1 : 1)) == -1 || *str == '+')
		str++;
	while (ft_isdigit(*str))
		n = n * 10 + (*str++ - '0');
	return (s * n);
}
